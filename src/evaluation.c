void initialize_piece_square_tables() {
    static_assert(PIECE_WHITE_KING == PIECE_BLACK_QUEEN + 1);

    // -2 because white and black pawns are the same, and the king has no value.
    static const Score piece_values_middle_game[PIECE_TYPE_COUNT - 2] = {82, 337, 365, 477, 1025};
    static const Score piece_values_end_game[PIECE_TYPE_COUNT - 2]    = {94, 281, 297, 512, 936};

    for (enum Piece piece = PIECE_WHITE_PAWN; piece < PIECE_WHITE_KING; ++piece) {
        for (enum Square square = SQUARE_A1; square < SQUARE_COUNT; ++square) {
            piece_square_middle_game[piece][square] += piece_values_middle_game[type_of_piece(piece)];
            piece_square_end_game[piece][square] += piece_values_end_game[type_of_piece(piece)];
        }
    }
}

Score evaluate_position(const struct Position* position) {
    assert(position != nullptr);

    // clang-format off
    static const int game_phase_increment[PIECE_TYPE_COUNT] = {
        [PIECE_TYPE_PAWN] = 0,
        [PIECE_TYPE_KNIGHT] = 1,
        [PIECE_TYPE_BISHOP] = 1,
        [PIECE_TYPE_ROOK] = 2,
        [PIECE_TYPE_QUEEN] = 4,
        [PIECE_TYPE_KING] = 0,
    };
    // clang-format on

    Score middle_game_score = DRAW_SCORE;
    Score end_game_score    = DRAW_SCORE;
    int game_phase          = 0;

    const Bitboard white_occupancy = piece_occupancy_by_color(position, COLOR_WHITE);
    const Bitboard black_occupancy = piece_occupancy_by_color(position, COLOR_BLACK);

    for (enum PieceType piece_type = PIECE_TYPE_PAWN; piece_type < PIECE_TYPE_BLACK_PAWN; ++piece_type) {
        const enum Piece white_piece        = create_piece(COLOR_WHITE, piece_type);
        const enum Piece black_piece        = create_piece(COLOR_BLACK, piece_type);
        const Bitboard piece_type_occupancy = piece_occupancy_by_type(position, piece_type);
        enum Square square;

        Bitboard white = piece_type_occupancy & white_occupancy;
        while (white != EMPTY_BITBOARD) {
            square = (enum Square)pop_lsb64(&white);
            middle_game_score += piece_square_middle_game[white_piece][square];
            end_game_score += piece_square_end_game[white_piece][square];
            game_phase += game_phase_increment[piece_type];
        }

        Bitboard black = piece_type_occupancy & black_occupancy;
        while (black != EMPTY_BITBOARD) {
            square = (enum Square)pop_lsb64(&black);
            middle_game_score -= piece_square_middle_game[black_piece][square];
            end_game_score -= piece_square_end_game[black_piece][square];
            game_phase += game_phase_increment[piece_type];
        }
    }

    /* Tapered eval. */
    int middle_game_phase = game_phase;
    if (middle_game_phase > 24)
        middle_game_phase = 24;  // In case of an early promotion.
    int end_game_phase = 24 - middle_game_phase;

    return (middle_game_score * middle_game_phase + end_game_score * end_game_phase) / 24;
}
